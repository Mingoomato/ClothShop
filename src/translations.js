// Language names
const LANGUAGE_NAMES = {
    ko: "한국어",
    en: "English",
    ja: "日本語",
    zh: "中文",
    vi: "Tiếng Việt"
};

/**
 * Global Translation Manager
 * Handles initializing, changing, and applying translations.
 */
const TranslationManager = {
    currentLang: 'ko', // default

    init() {
        // 1. Try to get from localStorage
        const storedLang = localStorage.getItem('vindteok_lang');
        if (storedLang && TRANSLATIONS[storedLang]) {
            this.currentLang = storedLang;
        } else {
            // 2. Or try to detect browser language
            const browserLang = navigator.language.slice(0, 2);
            if (TRANSLATIONS[browserLang]) {
                this.currentLang = browserLang;
            } else {
                this.currentLang = 'ko'; // Fallback
            }
        }

        // Initial application
        this.applyTranslation();
        this.updateSelectors();
    },

    changeLanguage(lang) {
        if (!TRANSLATIONS[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('vindteok_lang', lang);

        // Update DOM
        this.applyTranslation();
        this.updateSelectors();

        // Dispatch event for other components to react if needed
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    },

    get(key) {
        const dict = TRANSLATIONS[this.currentLang] || TRANSLATIONS['ko'];
        return dict[key] || key;
    },

    applyTranslation() {
        const dict = TRANSLATIONS[this.currentLang] || TRANSLATIONS['ko'];

        // 1. Text Content ([data-i18n])
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key]) {
                el.textContent = dict[key];
            }
        });

        // 2. Placeholders ([data-i18n-placeholder])
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (dict[key]) {
                el.placeholder = dict[key];
            }
        });

        // 3. Special handling for Select elements (Delivery Memo options)
        document.querySelectorAll('option[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key]) {
                el.textContent = dict[key];
            }
        });

        // Update HTML lang attribute
        document.documentElement.lang = this.currentLang;
    },

    updateSelectors() {
        // Update any language selector UI if it exists on page
        const currentLabel = document.querySelector('.lang-selector__current');
        if (currentLabel && LANGUAGE_NAMES[this.currentLang]) {
            currentLabel.textContent = LANGUAGE_NAMES[this.currentLang];
        }

        // Update active class in dropdowns
        document.querySelectorAll('.lang-option').forEach(btn => {
            if (btn.dataset.lang === this.currentLang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    }
};

// Expose to window
window.TRANSLATIONS = TRANSLATIONS;
window.LANGUAGE_NAMES = LANGUAGE_NAMES;
window.TranslationManager = TranslationManager;
